class PrimalExpert(nn.Module):
    """
    A protoplasmic neural network with no fixed structure.
    It receives its geometry (Weights) dynamically from the Hypernet.
    """
    def __init__(self, input_dim, hidden_dim, output_dim):
        super().__init__()
        self.input_dim = input_dim
        self.hidden_dim = hidden_dim
        self.output_dim = output_dim

    def forward(self, x, weights):
        # Dynamically inject weights generated by the Hypernetwork (Functional API)
        x = F.linear(x, weights['w1'], weights['b1'])
        x = torch.tanh(x)
        x = F.linear(x, weights['w2'], weights['b2'])
        return x

class NomadicHyperNet(nn.Module):
    """
    The 'Command Center' that translates the Will to Resonance (Phi) 
    into weight geometry.
    """
    def __init__(self, latent_dim, target_shapes):
        super().__init__()
        self.shapes = target_shapes
        total_params = sum(p.numel() for p in target_shapes.values())
        self.net = nn.Sequential(
            nn.Linear(latent_dim, 64),
            nn.LeakyReLU(0.2),
            nn.Linear(64, total_params)
        )

    def forward(self, z):
        flat_params = self.net(z)
        params = {}
        start = 0
        for name, shape in self.shapes.items():
            end = start + shape.numel()
            params[name] = flat_params[0, start:end].view(shape) # Batch dimension handling
            start = end
        return params

class ResonanceEngine(nn.Module):
    def __init__(self, input_dim=1, hidden_dim=16, output_dim=1):
        super().__init__()
        self.expert = PrimalExpert(input_dim, hidden_dim, output_dim)
        
        # Define the target weight shapes for the PrimalExpert
        self.target_shapes = {
            'w1': torch.Size([hidden_dim, input_dim]),
            'b1': torch.Size([hidden_dim]),
            'w2': torch.Size([output_dim, hidden_dim]),
            'b2': torch.Size([output_dim])
        }
        
        self.hypernet = NomadicHyperNet(latent_dim=2, target_shapes=self.target_shapes)
        
        # Latent space for preserving identity (The core of Phi)
        self.z = nn.Parameter(torch.randn(1, 2)) 
        self.stress_energy = 0.0 # Accumulated delta_x (Energy)
        self.tau_k_barrier = 1.5 # Threshold energy for Separatrix Collapse
        self.prev_weights = None

    def forward(self, x):
        weights = self.hypernet(self.z)
        return self.expert(x, weights), weights

    def check_collapse(self):
        """
        Separatrix Collapse: Structural transition when energy exceeds the barrier.
        Triggers a jump in the latent space.
        """
        if self.stress_energy > self.tau_k_barrier:
            with torch.no_grad():
                # Nomadic Jump: Destroying the current topology to find a new resonance
                self.z.add_(torch.randn_like(self.z) * 2.0) 
            self.stress_energy = 0.0 # Reset energy after collapse
            return True
        return False


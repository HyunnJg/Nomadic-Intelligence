            # Measure weight change (dW)
            current_w_flat = torch.cat([w.view(-1) for w in current_weights.values()])
            prev_w_flat = torch.cat([pw.view(-1) for pw in model.prev_weights.values()])
            dw = torch.norm(current_w_flat - prev_w_flat)
            
            # Dogma Penalty: If delta_x is high but dW is low, penalize rigidity

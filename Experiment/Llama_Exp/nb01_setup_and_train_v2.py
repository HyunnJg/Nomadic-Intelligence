def encode_pair(prompt, completion):
    full     = prompt + completion
    enc_full = tokenizer(full,   add_special_tokens=True, max_length=MAX_SEQ_LEN, truncation=True)
    enc_pmt  = tokenizer(prompt, add_special_tokens=True, max_length=MAX_SEQ_LEN, truncation=True)
    input_ids = enc_full['input_ids']
    labels    = [-100] * len(enc_pmt['input_ids']) + input_ids[len(enc_pmt['input_ids']):]
    labels    = labels[:len(input_ids)]
    return {
        'input_ids': torch.tensor(input_ids, dtype=torch.long),
        'labels':    torch.tensor(labels,    dtype=torch.long),
    }

def train_lora_expert(domain, pairs, save_dir):
    save_path = os.path.join(save_dir, f'lora_{domain}')
    if os.path.exists(save_path):
        print(f'  {domain}: 이미 존재, 스킵'); return save_path

    print(f'  {domain} LoRA 학습 중...')
    dataset    = [encode_pair(p, c) for p, c in pairs]
    model_prep = prepare_model_for_kbit_training(base_model)
    lora_model = get_peft_model(model_prep, get_lora_config())
    lora_model.train(); lora_model.print_trainable_parameters()
    optimizer = AdamW(lora_model.parameters(), lr=LORA_LR)
    losses = []

    for epoch in range(LORA_EPOCHS):
        epoch_loss = 0.0
        for sample in dataset:
            input_ids = sample['input_ids'].unsqueeze(0).to(base_model.device)
            labels    = sample['labels'].unsqueeze(0).to(base_model.device)
            optimizer.zero_grad()
            out  = lora_model(input_ids=input_ids, labels=labels)
            loss = out.loss
            loss.backward()
            torch.nn.utils.clip_grad_norm_(lora_model.parameters(), 1.0)
            optimizer.step()
            epoch_loss += loss.item()
        avg = epoch_loss / len(dataset); losses.append(avg)
        if (epoch + 1) % 2 == 0:
            print(f'    epoch {epoch+1:2d}/{LORA_EPOCHS} | loss={avg:.4f}')

    lora_model.save_pretrained(save_path)
    tokenizer.save_pretrained(save_path)
    print(f'  ✅ {domain} 저장: {save_path}')
    return save_path


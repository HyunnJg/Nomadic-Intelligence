def split_teacher_samples(samples, val_ratio=0.2, seed=42):
    rng = np.random.RandomState(seed)
    idx = np.arange(len(samples)); rng.shuffle(idx)
    n_val     = int(len(samples) * val_ratio)
    val_set   = set(idx[:n_val].tolist())
    train_s   = [s for i, s in enumerate(samples) if i not in val_set]
    val_s     = [s for i, s in enumerate(samples) if i in val_set]
    print(f'train={len(train_s)}, val={len(val_s)}')
    return train_s, val_s


def make_batches(samples, batch_size=32, shuffle=True, seed=42):
    idx = np.arange(len(samples))
    if shuffle:
        rng = np.random.RandomState(seed); rng.shuffle(idx)
    for start in range(0, len(idx), batch_size):
        yield [samples[i] for i in idx[start:start + batch_size]]


def policy_loss_fn(
    stay_switch_logits, target_logits, mode_logits,
    label_switch, label_target, label_mode, advantage,
    lambda_target=1.0, lambda_mode=0.3,
):
    """
    switch loss: advantage-weighted CE
      advantage = gap → 더 나은 expert가 있을수록 switch loss에 가중치
    """
    loss_switch_raw = F.cross_entropy(stay_switch_logits, label_switch, reduction='none')
    weight          = 1.0 + advantage                             # min=1, 큰 gap일수록 가중
    loss_switch     = (loss_switch_raw * weight).mean()
    loss_target     = F.cross_entropy(target_logits, label_target)
    loss_mode       = F.cross_entropy(mode_logits,   label_mode)
    total = loss_switch + lambda_target * loss_target + lambda_mode * loss_mode
    return total, {
        'switch': float(loss_switch.item()),
        'target': float(loss_target.item()),
        'mode':   float(loss_mode.item()),
    }


def evaluate_policy_net(policy_net, val_samples):
    policy_net.eval()
    y_true_sw, y_pred_sw = [], []
    y_true_tg, y_pred_tg = [], []
    stable_probs, trans_probs = [], []

    with torch.no_grad():
        for s in val_samples:
            hidden = s['hidden'].to(base_model.device)
            meta   = build_meta_signals_v2(s['meta'], num_experts=NUM_EXPERTS,
                                           device=base_model.device)
            sw_logits, tg_logits, _ = policy_net(hidden, meta)
            switch_prob = float(F.softmax(sw_logits, dim=-1)[0, 1].item())
            pred_sw     = int(switch_prob >= 0.5)
            pred_tg     = int(tg_logits.argmax(-1).item())

            y_true_sw.append(s['label_switch'])
            y_pred_sw.append(pred_sw)
            y_true_tg.append(s['label_target'])
            y_pred_tg.append(pred_tg)

            if s['label_switch'] == 0:
                stable_probs.append(switch_prob)
            else:
                trans_probs.append(switch_prob)

    switch_acc = float(np.mean(np.array(y_true_sw) == np.array(y_pred_sw)))
    target_acc = float(np.mean(np.array(y_true_tg) == np.array(y_pred_tg)))
    stable_mean = float(np.mean(stable_probs)) if stable_probs else float('nan')
    trans_mean  = float(np.mean(trans_probs))  if trans_probs  else float('nan')
    sep_gap     = trans_mean - stable_mean if (stable_probs and trans_probs) else float('nan')

    print(f'  [val] switch_acc={switch_acc:.4f} | target_acc={target_acc:.4f} | '
          f'switch_sep_gap={sep_gap:.4f} '
          f'(stable={stable_mean:.3f}, trans={trans_mean:.3f})')
    return {'switch_acc': switch_acc, 'target_acc': target_acc,
            'stable_mean': stable_mean, 'trans_mean': trans_mean,
            'switch_sep_gap': sep_gap}


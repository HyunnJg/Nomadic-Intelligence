def _unload_expert(m):
    del m; torch.cuda.empty_cache()

# ============================================================
# STEP 7: PolicyNet 교사 데이터 생성 — B방향 (1-step confidence gap)
# ============================================================
#
# [B방향 핵심 원칙]
# rollout_k = 4: 1-step next-token confidence를 모든 expert에서 계산
# → 훈련 교사 신호와 inference gap proxy가 완전히 동일한 방식으로 계산됨
# → train/inference 정합성 보장
#
# gap 정의:
#   confidence_score(expert) = 0.7 * top1_prob + 0.3 * (top1 - top2)
#   gap = max(0, best_expert_score - current_expert_score)
#
# switch label:
#   best_expert != current_expert AND gap > switch_cost → switch=1
# ============================================================

SWITCH_COST   = 0.08   # 낮게 시작 — 너무 switch 안 나오면 0.03으로 낮춤
HARD_MARGIN   = 0.15   # hard routing label threshold
STEPS_PER_PROMPT = 60  # 프롬프트당 수집 스텝


def compute_expert_confidence_score(logits: torch.Tensor) -> float:
    """
    1-step confidence score = 0.7 * top1_prob + 0.3 * (top1 - top2 margin)
    훈련과 inference 모두 이 함수를 공유.
    """
    probs = F.softmax(logits, dim=-1)
    top2  = probs.topk(2, dim=-1).values
    top1_prob = float(top2[0, 0].item())
    margin    = float((top2[0, 0] - top2[0, 1]).item())
    return 0.7 * top1_prob + 0.3 * margin


def compute_gap_and_label(
    ids: torch.Tensor,
    current_expert_key: str,
    expert_keys=EXPERT_KEYS,
    switch_cost=SWITCH_COST,
    hard_margin=HARD_MARGIN,
):
    """
    현재 context에서 모든 expert의 1-step confidence를 비교해
    gap, switch label, target label, mode label을 반환.

    B방향 핵심: 1-step confidence → inference의 gap proxy와 동일한 방식.
    """
    score_dict = {}
    for ek in expert_keys:
        m = _load_expert(ek)
        with torch.no_grad():
            out = m(ids)
        logits = out.logits[:, -1, :]
        score_dict[ek] = compute_expert_confidence_score(logits)
        _unload_expert(m)

    current_score = score_dict[current_expert_key]
    best_expert   = max(score_dict, key=score_dict.get)
    best_score    = score_dict[best_expert]

    gap = max(0.0, best_score - current_score)

    # switch label: 더 나은 expert가 있고 gap이 cost를 초과하면 switch
    label_switch = 1 if (best_expert != current_expert_key and gap > switch_cost) else 0
    label_target = expert_keys.index(best_expert)

    # mode label: score 차이가 hard_margin 초과면 hard routing
    sorted_scores = sorted(score_dict.values(), reverse=True)
    score_diff    = sorted_scores[0] - sorted_scores[1] if len(sorted_scores) >= 2 else 0.0
    label_mode    = 1 if score_diff > hard_margin else 0

    return gap, best_expert, label_switch, label_target, label_mode, score_dict


def build_policy_teacher_dataset_v2(
    train_prompts=TRAIN_PROMPTS,
    expert_keys=EXPERT_KEYS,
    steps_per_prompt=STEPS_PER_PROMPT,
    switch_cost=SWITCH_COST,
    hard_margin=HARD_MARGIN,
    min_prefix_len=6,
):
    """
    B방향 teacher dataset 생성.
    각 position에서 1-step confidence gap으로 레이블 생성.
    warm-start expert는 해당 domain expert로 설정.
    """
    samples = []

    for domain, prompt_list in train_prompts.items():
        print(f'  [teacher] domain={domain}')

        for text in prompt_list:
            ids_all = tokenizer(text, return_tensors='pt').input_ids.to(base_model.device)

            if ids_all.shape[1] < min_prefix_len + 2:
                continue

            tracker        = HybridDeltaTracker()
            current_expert = domain           # warm-start: 해당 도메인 expert
            dwell_count    = 1

            # min_prefix_len 이후 위치에서 steps_per_prompt개 샘플링
            max_t       = ids_all.shape[1] - 1
            positions   = list(range(min_prefix_len, max_t))[:steps_per_prompt]

            for t in positions:
                prompt_ids = ids_all[:, :t]

                # 현재 expert로 hidden state + logits 추출
                cur_model = _load_expert(current_expert)
                with torch.no_grad():
                    out = cur_model(prompt_ids, output_hidden_states=True)
                hidden = out.hidden_states[-1][:, -1, :].detach()
                logits = out.logits[:, -1, :].detach()
                probs  = F.softmax(logits, dim=-1)
                conf   = float(probs.max().item())
                logit_margin = top1_minus_top2_margin(logits)
                err    = 1.0 - conf
                rec    = tracker.compute(hidden, err)
                _unload_expert(cur_model)

                # 1-step confidence gap으로 레이블 생성 (B방향 핵심)
                gap, best_expert, label_switch, label_target, label_mode, score_dict = \
                    compute_gap_and_label(
                        ids=prompt_ids,
                        current_expert_key=current_expert,
                        expert_keys=expert_keys,
                        switch_cost=switch_cost,
                        hard_margin=hard_margin,
                    )

                dwell_ratio = dwell_count / max(rec['tau_dyn'], 1e-6)

                meta = {
                    'delta_env':         rec['delta_env'],
                    'delta_err':         rec['delta_err'],
                    'delta_hybrid':      rec['delta_hybrid'],
                    'sigma2':            rec['sigma2'],
                    'tau_dyn':           rec['tau_dyn'],
                    'dwell_ratio':       dwell_ratio,
                    'current_conf':      conf,
                    'logit_margin':      logit_margin,
                    'gap':               gap,
                    'current_expert_id': expert_keys.index(current_expert),
                }

                advantage = gap  # advantage = 전환했을 때 expected gain proxy

                samples.append({
                    'hidden':       hidden.cpu(),
                    'meta':         meta,
                    'label_switch': label_switch,
                    'label_target': label_target,
                    'label_mode':   label_mode,
                    'advantage':    float(advantage),
                    'domain':       domain,
                    'current_expert': current_expert,
                    'best_expert':  best_expert,
                })

                # teacher policy로 상태 업데이트
                if label_switch == 1:
                    current_expert = best_expert
                    dwell_count    = 1
                else:
                    dwell_count += 1

    print(f'✅ teacher samples: {len(samples)}')

    # switch label 분포 확인
    n_switch = sum(1 for s in samples if s['label_switch'] == 1)
    print(f'   switch=1: {n_switch} ({100*n_switch/max(len(samples),1):.1f}%)')
    print(f'   switch=0: {len(samples)-n_switch} ({100*(len(samples)-n_switch)/max(len(samples),1):.1f}%)')
    if n_switch / max(len(samples), 1) < 0.1:
        print('   ⚠️  switch=1 비율이 낮음. switch_cost를 낮추는 것을 고려하세요.')
    elif n_switch / max(len(samples), 1) > 0.7:
        print('   ⚠️  switch=1 비율이 높음. switch_cost를 높이는 것을 고려하세요.')

    return samples

